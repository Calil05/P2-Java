package Uber;

// Encapsulamento Utilizado
// Polimorfismo Utilizado

public class Pessoa {

    private String Nome;
    private int Idade;
    private int CPF;

    public Pessoa() {

    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public int getIdade() {
        return Idade;
    }

    public void setIdade(int idade) {
        Idade = idade;
    }

    public int getCPF() {
        return CPF;
    }

    public void setCPF(int cPF) {
        CPF = cPF;
    }

    
    
}
